    private void btpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesquisarActionPerformed
        // TODO add your handling code here:
        txtpesquisa.requestFocus();
        boolean ok = true;
        if (txtpesquisa.getText() == null) {
            txtpesquisa.setText("");
        }

        if (ok) {
            DefaultTableModel model = iniciaTabela();
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();

            String campo = "id";
            if (!ccampo.getSelectedItem().toString().equals("CODIGO")) {
                campo = ccampo.getSelectedItem().toString().toLowerCase();
            }

            List<Produto> lista = new ProdutoController().pesquisar(txtpesquisa.getText(), campo);
            lista.forEach((u) -> {
                model.addRow(new Object[]{u.getId(),
                    u.getDescricao(),
                    Util.numero.format(u.getValordia()),
                    u.getStatus(),
                    u.getEntrada(),
                    u.getSaida(),
                    u.getFornecedor(),
                    u.getContato(),
                    Util.datahoralegivel.format(u.getDataentrada()),
                    u.getUsuarioAltera(),
                    Util.datahoralegivel.format(u.getDataAltera())
                });
            });
            model.fireTableDataChanged();
            tabela.setDefaultEditor(Object.class, null);

        }
    }//GEN-LAST:event_btpesquisarActionPerformed

