    private DefaultTableModel iniciaTabela() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tabela.setRowSorter(sorter);
        return model;
    }

